package com.demo.model;

public class ParamConfig {

	private String config_name;
	private String config_desc;
	private String config_value;
	private String config_index;
	public String getConfig_name() {
		return config_name;
	}
	public void setConfig_name(String config_name) {
		this.config_name = config_name;
	}
	public String getConfig_desc() {
		return config_desc;
	}
	public void setConfig_desc(String config_desc) {
		this.config_desc = config_desc;
	}
	public String getConfig_value() {
		return config_value;
	}
	public void setConfig_value(String config_value) {
		this.config_value = config_value;
	}
	public String getConfig_index() {
		return config_index;
	}
	public void setConfig_index(String config_index) {
		this.config_index = config_index;
	}
	
	
}
